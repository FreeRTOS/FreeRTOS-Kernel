/**
 * @brief Critical section management.
 */
#define portDISABLE_INTERRUPTS()            __asm volatile ( " cpsid i " ::: "memory" )
#define portENABLE_INTERRUPTS()             __asm volatile ( " cpsie i " ::: "memory" )
